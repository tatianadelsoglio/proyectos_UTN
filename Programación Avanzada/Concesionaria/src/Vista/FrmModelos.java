    public void desactivarSessionActualizacion(){
        cboMarcaActualizar.setEnabled(false);
        txtNombreModeloActualizar.setEnabled(false);
        btnActualizar.setEnabled(false);
        btnCancelar.setEnabled(false);
        
        cboMarcasDeModelo.setEnabled(true);
        txtNombreNuevoModelo.setEnabled(true);
        btnGuardar.setEnabled(true);
        
        cboMarcaBusqueda.setEnabled(true);
        txtNombreMBusqueda.setEnabled(true);
        btnBuscar.setEnabled(true);
        tablaModelos.setEnabled(true);
        btnMostrarTodos.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnEditar.setEnabled(true);
        
        txtIdActualizarModelo.setText(null);
        txtNombreModeloActualizar.setText(null);
        cboMarcaActualizar.removeAllItems();
    }
    
    public void activarSessionActualizacion(){
        cboMarcaActualizar.setEnabled(true);
        txtNombreModeloActualizar.setEnabled(true);
        btnActualizar.setEnabled(true);
        btnCancelar.setEnabled(true);
        
        cboMarcasDeModelo.setEnabled(false);
        txtNombreNuevoModelo.setEnabled(false);
        btnGuardar.setEnabled(false);
        
        cboMarcaBusqueda.setEnabled(false);
        txtNombreMBusqueda.setEnabled(false);
        btnBuscar.setEnabled(false);
        tablaModelos.setEnabled(false);
        btnMostrarTodos.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnEditar.setEnabled(false);
    }
