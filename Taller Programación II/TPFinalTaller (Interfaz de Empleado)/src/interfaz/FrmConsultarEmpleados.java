    private void cargarTabla() {
        String[] encabezado = {"Nombre", "Apellido", "Rol"};
        DefaultTableModel modelo = new DefaultTableModel(null, encabezado);
        for (Empleado e : this.miGestor.getEmpleadosDelSistema()) {
            Object[] linea = {e, e.getApellido(), e.getRol()};
            modelo.addRow(linea);
        }
        tblEmpleados.setModel(modelo);
        
        
    }

