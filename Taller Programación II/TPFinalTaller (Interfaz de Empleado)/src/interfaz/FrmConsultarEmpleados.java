    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblEmpleados.getSelectedRow() >= 0) {
            Empleado empleado = null;
            empleado = (Empleado) tblEmpleados.getModel().getValueAt(tblEmpleados.getSelectedRow(), 0);
            this.miGestor.mostrarPantallaEditarEmpleado(empleado);
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un empleado", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      // TODO add your handling code here
      
        DefaultTableModel dtm = (DefaultTableModel) tblEmpleados.getModel(); 
    if(tblEmpleados.getSelectedRow() != -1) {
        dtm.removeRow(tblEmpleados.getSelectedRow()); 
    } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un empleado", "Error", JOptionPane.ERROR_MESSAGE);
        }
      
      
    }//GEN-LAST:event_btnEliminarActionPerformed


