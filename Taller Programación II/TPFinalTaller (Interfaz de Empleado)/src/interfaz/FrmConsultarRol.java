        if (tblRol.getSelectedRow() >= 0) {
            Rol rolSeleccionado = null;
        rolSeleccionado = (Rol) tblRol.getModel().getValueAt(tblRol.getSelectedRow(), 0);
        this.miGestor.mostrarPantallaEditarRol(rolSeleccionado);
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un empleado", "Error", JOptionPane.ERROR_MESSAGE);
        }
