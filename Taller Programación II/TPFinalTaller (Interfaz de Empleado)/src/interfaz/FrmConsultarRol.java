     private void cargarTabla() {
        tblRol.removeAll();
        String[] encabezado = {"Nombre", "Descripcion"};
        DefaultTableModel modelo = new DefaultTableModel(null, encabezado);
        for (Rol r : this.miGestor.getRolesDelSistema()) {
            Object[] linea = {r, r.getDescripcion()};
            modelo.addRow(linea);
        }
        tblRol.setModel(modelo);
     }
     
