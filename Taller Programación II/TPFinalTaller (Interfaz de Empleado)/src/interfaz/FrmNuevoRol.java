    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:       
         
         String nombreRol = this.txtNombreRol.getText();
         
        if (nombreRol != null && nombreRol.length() > 0) {
            if (modo == 0) { // NUEVO
                Rol nuevoRol = new Rol(nombreRol, this.txtDescripcion.getText());
                this.miGestor.guardarNuevoRol(nuevoRol);
            } else { // EDITAR
                this.instanciaEditando.setNombreRol(txtNombreRol.getText());
                this.instanciaEditando.setDescripcion(txtDescripcion.getText());
                this.miGestor.editarRol(this.instanciaEditando);
                txtNombreRol.setText("");
                txtDescripcion.setText("");
            }

            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Verifique los datos requeridos", "Atenci√≥n", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed


